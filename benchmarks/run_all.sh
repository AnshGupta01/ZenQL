#!/bin/bash

# Clear any old benchmark reports from the README if they exist
sed -i.bak '/## 📊 Automated Benchmark Reports/,$d' README.md
rm -f README.md.bak

# Setup the Benchmark Section table header in README
echo "## 📊 Automated Benchmark Reports" >> README.md
echo "The following results were automatically generated by \`benchmarks/run_all.sh\` testing the Thread Pool, HashMap Lookups, and TCP Protocol Pipelining architectures:" >> README.md
echo "" >> README.md
echo "| Rows Inserted | Concurrent Reads Survived | Elapsed Time | Inserts/Second |" >> README.md
echo "|--------------:|--------------------------:|-------------:|---------------:|" >> README.md

echo "Starting massive automated benchmark suite!"
echo "--------------------------------------------------------"

for ROWS in 100 1000 10000 100000 1000000 10000000; do
    echo "Executing benchmark for $ROWS rows..."
    
    # Capture the output from the benchmark executable into a variable
    OUTPUT=$(./bin/flexql_benchmark $ROWS | tail -n 5)
    
    # Parse the text strings to extract just the pure numbers
    READS=$(echo "$OUTPUT" | grep "Total Concurrent Reads" | grep -oE '[0-9]+' | head -n 1)
    TIME=$(echo "$OUTPUT" | grep "Total Elapsed Time" | grep -oE '[0-9.]+' | head -n 1)
    TPS=$(echo "$OUTPUT" | grep "Throughput (Inserts)" | awk '{print $3}') 
    
    # In case there were 0 reads or the run was too fast
    if [ -z "$READS" ]; then READS="0"; fi
    if [ -z "$TIME" ]; then TIME="< 0.01"; fi
    if [ -z "$TPS" ]; then TPS="N/A"; fi

    # Map string for English labels
    case $ROWS in
        100) LABEL="100" ;;
        1000) LABEL="1 Thousand" ;;
        10000) LABEL="10 Thousand" ;;
        100000) LABEL="100 Thousand" ;;
        1000000) LABEL="1 Million" ;;
        10000000) LABEL="10 Million" ;;
        *) LABEL="$ROWS" ;;
    esac

    # Log the row neatly into the Markdown Table
    echo "| **$LABEL** | $READS | ${TIME}s | **$TPS** |" >> README.md
    
    sleep 1 # Let the server breathe
done

echo "--------------------------------------------------------"
echo "✅ All tests exclusively finished. View your README.md to see the final markdown table!"
